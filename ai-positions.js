// Генерация ДОПОЛНИТЕЛЬНЫХ позиций под формат ВЗП через vision-модель (api.wellflow.dev).
// База — 5 позиций (5x5). При забиве NxN (N>5) ИИ добавляет (N-5) позиций по той же логике,
// что использовалась для ручной разметки карт.
//
// ВКЛЮЧЕНИЕ: требует поднятого gateway и vision-модели в .env WELLFLOW_MODEL_HEAVY
// (напр. claude-opus-4.6 / claude-haiku-4.5 — обе с vision). Сейчас gateway лежит → вернёт [].
import fs from 'fs';
import { aiChat, aiEnabled, HEAVY_MODEL } from './ai.js';

// Вся логика расстановки, которую мы применяли к фото карт:
const SYSTEM_PROMPT = `Ты — тактик ВЗП (война за точку, GTA5RP) семьи .aeterna. Тебе дают спутниковый снимок карты и базовые позиции (5 шт). Нужно добавить позиции под больший формат.

ОБЩИЕ ПРАВИЛА ВЗП:
- Защита держит зону и НЕ выходит за неё (снаружи большой урон). Цель — не пустить атаку в зону, пустили — убить всех.
- Атака заходит в зону и убивает всех. Первые 6 минут вне зоны без урона (занимают позиции), после 6 минут вне зоны — большой урон.
- Атака может заходить с любой стороны, но реально заходит оттуда, где есть укрытие/высота.

ПРИОРИТЕТ ВЫБОРА ПОЗИЦИИ (строго в этом порядке):
1) РЕЛЬЕФ — место, откуда можно играть «от головы»: право-пик (выходишь правым плечом — тебя почти не видно, врага видишь полностью) или лево-пик с упором в стену.
2) ПИК — выгодный угол на вероятные подходы врага.
3) ЗАКРЫТОСТЬ — не быть полностью открытым; крыши, углы зданий, за укрытием.

ЛОГИКА РАССТАНОВКИ:
- ЗАЩИТА: плотный кластер у центра зоны / на крышах и укрытиях; сектора обзора (конусы) веером наружу покрывают ВСЕ подходы; мутуал-саппорт — позиции близко друг к другу, чтобы одиночку не зажали; НЕ стоять в открытом поле/дворе.
- АТАКА: 2 группы (кластера) близко к краю зоны на стороне захода, из укрытий/крыш/высоты; НЕ стоять внутри зоны до боя; заходить там, где есть укрытие (сверху, с домов, с высоты), не по открытому.
- ДОП. ПОЗИЦИИ: добавляй рядом с существующими кластерами, на укрытиях/крышах, сохраняя плотность и мутуал-саппорт; не разноси далеко.

ФОРМАТ ОТВЕТА — строго JSON, без пояснений:
{"extra":[{"x":0.00,"y":0.00,"cone":0}]}
где x,y — доли ширины/высоты снимка (0..1), cone — угол сектора обзора в градусах (0=восток,90=низ,180=запад,270=верх) для защиты, или null для атаки.`;

/**
 * @param {object} o
 * @param {'ЗАЩИТА'|'АТАКА'} o.side
 * @param {string} o.mapFile  путь к png карты
 * @param {number[][]} o.basePositions  базовые позиции [[x,y],...]
 * @param {number} o.format  N из NxN (5,6,7,8...)
 * @returns {Promise<Array<[number,number,(number|null)]>>}  доп. позиции
 */
export async function generateExtraPositions({ side, mapFile, basePositions, format }) {
  const need = Math.max(0, (format || 5) - 5);
  if (need === 0) return [];
  if (!aiEnabled()) {
    console.warn('[ai-pos] ИИ выключен (gateway/модель не настроены) — доп. позиции не сгенерированы');
    return [];
  }
  let b64;
  try { b64 = fs.readFileSync(mapFile).toString('base64'); }
  catch (e) { console.error('[ai-pos] нет файла карты:', e.message); return []; }

  const userContent = [
    {
      type: 'text',
      text:
        `Сторона: ${side}. Формат ${format}x${format}. Базовые позиции (доли 0..1): ` +
        `${JSON.stringify(basePositions)}. Добавь ровно ${need} НОВЫХ позиций по логике выше. ` +
        `Координаты — доли ширины/высоты ЭТОГО снимка. Только JSON.`,
    },
    { type: 'image_url', image_url: { url: `data:image/png;base64,${b64}` } },
  ];

  const res = await aiChat(
    [{ role: 'system', content: SYSTEM_PROMPT }, { role: 'user', content: userContent }],
    { model: HEAVY_MODEL, maxTokens: 700, temperature: 0.3 },
  );
  if (!res) return [];

  try {
    const m = res.match(/\{[\s\S]*\}/);
    const data = JSON.parse(m ? m[0] : res);
    return (data.extra || [])
      .filter((p) => typeof p.x === 'number' && typeof p.y === 'number')
      .slice(0, need)
      .map((p) => [p.x, p.y, (typeof p.cone === 'number' ? p.cone : null)]);
  } catch (e) {
    console.error('[ai-pos] не распарсил ответ ИИ:', e.message);
    return [];
  }
}
