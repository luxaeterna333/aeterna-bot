const client = new Client({ intents: [GatewayIntentBits.Guilds] });
await client.login(process.env.TOKEN);
await new Promise((r) => client.once('ready', r));
const channel = await client.channels.fetch(MAPS_CHANNEL_ID);

// Чистим старые сообщения бота
let fetched;
do {
  fetched = await channel.messages.fetch({ limit: 100 });
  for (const m of fetched.values()) if (m.author.id === client.user.id) await m.delete().catch(() => {});
} while (fetched.size === 100);

// Правила
await channel.send({ embeds: [new EmbedBuilder()
  .setTitle('🗺️ ВЗП — правила и тактика')
  .setColor(0xe67e22)
  .setDescription(
    '🛡️ **Защита** — не пустить в зону; пустили — убить всех. **Не выходить за зону**.\n' +
    '⚔️ **Атака** — зайти в зону, убить всех. Может заходить **с любой стороны**.\n\n' +
    '⏱️ Первые **6 мин** атака вне зоны без урона (занимаем позиции). После — **большой урон** вне зоны.\n\n' +
    '🔵 Защита: цифры = позиции, конусы = сектор обзора. 🔴 Атака: цифры = позиции захода, стрелки = маршруты.\n' +
    'Приоритет позиций: **рельеф (право/лево-пик) → пик → закрытость**. Позиции кучно (мутуал-саппорт).'
  )] });

// По карте: описание + обычная (базовая) карта
for (const fname of Object.keys(specs)) {
  const stem = fname.replace('.png', '');
  const base = path.join(__dirname, fname); // обычная карта, без разметки 5х5
  if (!fs.existsSync(base)) { console.log('skip', stem); continue; }
  const d = DESC[stem] || { a: '—', d: '—' };
  const embed = new EmbedBuilder()
    .setTitle(`🗺️ ${stem}`)
    .setColor(0x3498db)
    .addFields(
      { name: '⚔️ Атака', value: d.a, inline: false },
      { name: '🛡️ Защита', value: d.d, inline: false },
    )
    .setImage(`attachment://${fname.replace(/\s/g, '_')}`);
  await channel.send({ embeds: [embed], files: [new AttachmentBuilder(base).setName(fname.replace(/\s/g, '_'))] });
  console.log('posted', stem.replace(/[^\x00-\x7F]/g, '?'));
}
console.log('DONE');
await client.destroy();
